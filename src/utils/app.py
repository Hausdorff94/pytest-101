def sum_function(a_num: float, b_num: float) -> float:
    """
    Sum two numbers

    Parameters
    ----------
    a : float
        First number
    b : float
        Second number

    Returns
    -------
    float
        Sum of a and b

    """

    return a_num + b_num


def sub_function(a_num: float, b_num: float) -> float:
    """
    Sub two numbers

    Parameters
    ----------
    a : float
        First number
    b : float
        Second number

    Returns
    -------
    float
        Sum of a and b

    """

    return a_num - b_num
